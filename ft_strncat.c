#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t m;

	i = 0;
	m = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[m] != '\0' && m < n)
	{
		s1[i + m] = s2[m];
		m++;
	}
	s1[i + m] = '\0';
	return (s1);
}
